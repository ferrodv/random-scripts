import * as path from 'path';
import * as os from 'os';
import csvToJson from "convert-csv-to-json";
import request from "../utils/request";
import response from "../utils/response";

export async function handler(event: any) {
  const { fullCodesUrl } = request(event.body);

  try {
    const csvPath = path.join(os.homedir(), 'Downloads', fullCodesUrl);
    const codes = csvToJson.fieldDelimiter(',').getJsonFromCsv(csvPath);

    const codesToBuy: any[] = [];
    const error: any[] = [];
    codes.forEach(codeObj => {
      if (!bougthCodes.some(bougthCode => {
        if (codeObj.RowLabels == bougthCode[0])
          return true;
        return false;
      })) {
        let isPushed = false;
        for (const [state, areaCodes] of Object.entries(stateAreaCodes)) {
          if (areaCodes.includes(codeObj.RowLabels)) {
            isPushed = true;
            return codesToBuy.push([codeObj.RowLabels, state]);
          }
        };
        if (isPushed == false) error.push(codeObj.RowLabels);
      }
    })
    let testCodes: any[] = []
    bougthCodes.forEach(bougthCode => {
      if (!codes.some(code => {
        if (code.RowLabels == bougthCode[0]) return true;
        return false;
      })) testCodes.push(bougthCode);

    })

    return response({ testCodesCount: testCodes.length, testCodes, error, codeCount: codes.length, codesToBuyCount: codesToBuy.length, bougthCodes: bougthCodes.length, codesToBuy });
  } catch (e) {
    console.error(e);
    return response({ message: "Not ok" }, 503);
  }
}




